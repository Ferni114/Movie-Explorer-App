class MovieDetailEntity {
  final int id;
  final String poster;
  final String title;
  final String date;
  final String ranking;
  final String synopsis;
  final String genre;

  MovieDetailEntity({
    required this.id,
    required this.poster,
    required this.title,
    required this.date,
    required this.ranking,
    required this.synopsis,
    required this.genre,
  });
}
